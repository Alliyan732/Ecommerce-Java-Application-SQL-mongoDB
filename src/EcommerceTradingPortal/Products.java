    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        // TODO add your handling code here:
        if(productId.getText().isEmpty() || productName.getText().isEmpty() || productPrice.getText().isEmpty() || productQty.getText().isEmpty() || productDescription.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Incomplete Information!");
        }
        else{
            try {
                conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");

                String query = "Update PRODUCT set product_name='"+productName.getText()+"'"+",product_quantity='"+productQty.getText()+"'"+","
                + "product_category='"+productCategory.getSelectedItem().toString()+"'"+",product_price='"+ productPrice.getText()+"'"+","
                + "product_description='"+ productDescription.getText()+"'"+ "where product_id="+productId.getText();

                Statement add = conn.createStatement();
                add.executeQuery(query);
                JOptionPane.showMessageDialog(this, "Product Updated!");
                displayProduct();

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked

