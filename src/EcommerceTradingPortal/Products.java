    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        if(productId.getText().isEmpty() || productName.getText().isEmpty()
            || productQty.getText().isEmpty()
            || productPrice.getText().isEmpty()
            || productDescription.getText().isEmpty()){

            JOptionPane.showMessageDialog(this, "Information is Missing!");

        }
        else{
            try{
                Class.forName("oracle.jdbc.driver.OracleDriver");
                Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl","hr","hr");
                PreparedStatement add = conn.prepareStatement("insert into product values(?,?,?,?,?,?)");
                add.setInt(1, Integer.valueOf(productId.getText()));
                add.setString(2, productName.getText());
                add.setInt(3, Integer.valueOf(productQty.getText()));
                add.setInt(4, Integer.valueOf(productPrice.getText()));
                add.setString(5, productCategory.getSelectedItem().toString());
                add.setString(6, productDescription.getText());
                JOptionPane.showMessageDialog(this, "Product Added Successfully!");
                add.executeQuery();
                // to display seller in table, we are calling a displaySeller method
                displayProduct();
                conn.close();

            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addBtnMouseClicked

